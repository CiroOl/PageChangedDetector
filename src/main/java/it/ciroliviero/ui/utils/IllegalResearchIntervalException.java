package it.ciroliviero.ui.utils;

public class IllegalResearchIntervalException extends Exception {

	public IllegalResearchIntervalException() {
		super(); 
	}

	public IllegalResearchIntervalException(String message) {
		super(message); 
	}

	public IllegalResearchIntervalException(String message, Throwable cause) {
		super(message, cause); 
	}
	public IllegalResearchIntervalException(Throwable cause) {
		super(cause); 
	}

}
